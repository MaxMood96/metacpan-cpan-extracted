package IO::K8s::KubeAggregator::Pkg::Apis::Apiregistration::V1::APIServiceCondition;
# ABSTRACT: APIServiceCondition describes the state of an APIService at a particular point
our $VERSION = '1.001';
use IO::K8s::Resource;

k8s lastTransitionTime => Str;


k8s message => Str;


k8s reason => Str;


k8s status => Str, 'required';


k8s type => Str, 'required';


1;

__END__

=pod

=encoding UTF-8

=head1 NAME

IO::K8s::KubeAggregator::Pkg::Apis::Apiregistration::V1::APIServiceCondition - APIServiceCondition describes the state of an APIService at a particular point

=head1 VERSION

version 1.001

=head2 lastTransitionTime

Last time the condition transitioned from one status to another.

=head2 message

Human-readable message indicating details about last transition.

=head2 reason

Unique, one-word, CamelCase reason for the condition's last transition

=head2 status

Status is the status of the condition. Can be True, False, Unknown.

=head2 type

Type is the type of the condition.

