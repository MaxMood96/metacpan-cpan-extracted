package IO::K8s::Api::Authorization::V1::NonResourceRule;
# ABSTRACT: NonResourceRule holds information that describes a rule for the non-resource
our $VERSION = '1.002';
use IO::K8s::Resource;

k8s nonResourceURLs => [Str];


k8s verbs => [Str], 'required';


1;

__END__

=pod

=encoding UTF-8

=head1 NAME

IO::K8s::Api::Authorization::V1::NonResourceRule - NonResourceRule holds information that describes a rule for the non-resource

=head1 VERSION

version 1.002

=head2 nonResourceURLs

NonResourceURLs is a set of partial urls that a user should have access to.  *s are allowed, but only as the full, final step in the path.  "*" means all.

=head2 verbs

Verb is a list of kubernetes non-resource API verbs, like: get, post, put, delete, patch, head, options.  "*" means all.

