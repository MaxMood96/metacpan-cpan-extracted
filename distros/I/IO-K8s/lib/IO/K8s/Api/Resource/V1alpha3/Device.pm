=head1 NAME

IO::K8s::Api::Resource::V1alpha3::Device - Device represents one individual hardware instance that can be selected based on its attributes. Besides the name, exactly one field must be set.

=head1 VERSION

version 1.001

=head2 basic

Basic defines one device instance.

=head2 name

Name is unique identifier among all devices managed by the driver in the pool. It must be a DNS label.

