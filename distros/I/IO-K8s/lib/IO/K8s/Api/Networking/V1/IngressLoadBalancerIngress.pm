package IO::K8s::Api::Networking::V1::IngressLoadBalancerIngress;
# ABSTRACT: IngressLoadBalancerIngress represents the status of a load-balancer ingress point.
our $VERSION = '1.002';
use IO::K8s::Resource;

k8s hostname => Str;


k8s ip => Str;


k8s ports => ['Networking::V1::IngressPortStatus'];


1;

__END__

